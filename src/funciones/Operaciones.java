package funciones;

public class Operaciones {
	
	protected static double sumar(int num1, int num2) {
		return num1 + num2;
	}
	 
	protected static double restar(int num1, int num2) {
		return num1 - num2; 
	}
	
	protected static double multiplicar(int num1, int num2) {
		return num1 * num2;
	}
	
	protected static double dividir(int num1, int num2) {
		return num1 / num2;
	}
	
}
